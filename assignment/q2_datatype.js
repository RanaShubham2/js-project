// Q12. Write a program that demonstrates all 6 primitive data types in JavaScript with real-world examples for
// each.

// String datatype


let userName = "Shubham";
console.log("String Example:");
console.log("User Name:", userName);
console.log("Type:", typeof userName);

console.log("--------------------------------");

// 2. Number

let productPrice = 999.99;
console.log("Number Example:");
console.log("Product Price:", productPrice);
console.log("Type:", typeof productPrice);

console.log("--------------------------------");

// 3. Boolean

let isLoggedIn = true;
console.log("Boolean Example:");
console.log("Is User Logged In:", isLoggedIn);
console.log("Type:", typeof isLoggedIn);

console.log("--------------------------------");

// 4. Undefined

let userAddress;
console.log("Undefined Example:");
console.log("User Address:", userAddress);
console.log("Type:", typeof userAddress);

console.log("--------------------------------");

// 5. Null

let bankAccount = null;
console.log("Null Example:");
console.log("Bank Account:", bankAccount);
console.log("Type:", typeof bankAccount);

console.log("--------------------------------");

// 6. Symbol

let uniqueID = Symbol("id");
console.log("Symbol Example:");
console.log("Unique ID:", uniqueID);
console.log("Type:", typeof uniqueID);
